#include "libft.h"

char	*ft_strndup(const char *s1, int n)
{
	int		i;
	char	*s2;

	if (!(s2 = (char*)malloc(sizeof(*s1) * n + 1)))
		return (NULL);
	i = 0;
	while (s1[i] && n)
	{
		s2[i] = s1[i];
		i++;
		n--;
	}
	s2[i] = '\0';
	return (s2);
}
