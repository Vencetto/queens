#include "../libft.h"

void	ft_uputnbr_m(uintmax_t n)
{
	if (n > 9223372036854775807)
		return (ft_uhuge_mzfk(n));
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, 1);
		ft_putchar_fd(n % 10 + '0', 1);
	}
	else
		ft_putchar_fd(n + '0', 1);
}
