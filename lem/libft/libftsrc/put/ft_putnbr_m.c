#include "../libft.h"

void	ft_putnbr_m(intmax_t n)
{
	if (n > 9223372036854775807 || n < -9223372036854775807)
		return (ft_huge_mzfk(n));
	if (n < 0)
		ft_putnbr_m(-n);
	else if (n >= 10)
	{
		ft_putnbr_m(n / 10);
		ft_putchar_fd(n % 10 + '0', 1);
	}
	else
		ft_putchar_fd(n + '0', 1);
}
