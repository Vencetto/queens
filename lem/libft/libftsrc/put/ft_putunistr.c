#include "../libft.h"

int		ft_putunistr(wchar_t *s)
{
	int	i;
	int	len;

	i = 0;
	len = 0;
	if (s == NULL)
		return (0);
	while (s[i])
	{
		len += put_wchar(s[i]);
		i++;
	}
	return (len);
}
